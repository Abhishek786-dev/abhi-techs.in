  <h3>Add password to Lock Screen</h3>
</center>
<ul>
  <li>For security reasons, you may want to put the password to your screen session. A Password will be asked whenever you want to re-attach the screen. This password is different from the Lock Screen mechanism above.
  </li>
  <li>
    To make your screen password protected, you can edit the “$HOME/.screenrc” file. If the file doesn’t exist, you can create it manually. The syntax will be like this.
  </li>

  ex : password crypt_password
  <li>
    To create “crypt_password” above, you can use the “mkpasswd” command on Linux. Here’s the command with the password “pungki123“.
