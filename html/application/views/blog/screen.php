    <h1>Linux Screen </h1>
  <p>
    Linux Screen commands provides multiple shell session  from single ssh session, when you want to run mutiple process/ multiple project at time , that time you can use screen command. 
  </p>  
<center>
  <h3>Install Linux GNU Screen</h3>
</center>

<p>
  1. It's default package in most the linux flever, you can check a veriosn using following command 
</p>
<pre>
  <code class="language-bash">abhishek@abhishek:~ $ screen --version
  Screen version 4.08.02 (GNU) 05-Feb-20
  </code>
</pre>

<p>2. If you don't have  install , so you can install screen package using below command </p>

<pre>
  <code class="language-bash">
    abhishek@abhishek:~ $ sudo apt update
    abhishek@abhishek:~ $ sudo apt install screen
  </code>
</pre>

<center>
  <h3>Working with screen</h3>
</center>
<ul>
<li>
    1. simple using screen command you will jump on to a specific session.
</li>
<li>
    2. You can have multiple windows inside a Screen session.
</li>
<li>
    below are some most common commands for managing Linux Screen Windows:
</li>
<li>Ctrl+a c  : Create a new window (with shell).</li>
<li>Ctrl+a "  : list all windoes  </li>
<li>Ctrl+a 0  : Switch to window 0 (by number). </li>
<li>Ctrl+a A  : Rename the current window.</li>
<li>Ctrl+a S  : Split current region horizontally into two regions</li>
<li>Ctrl+a |  : Split current region vertically into two regions</li>
<li>Ctrl-a + w: It display the list of all the windows currently opened.</li>
<li>Ctrl-a + n: It go to the next windows.</li>
<li>Ctrl-a + p: It go to the previous windows.</li>
</ul>
<center>
  <h3>Screen  Commands</h3>
</center>
<ul>
  <li>1. screen -S "session_name" : if you want to give a name to your session windoes then you can use command like this</li>
  <li>2. screen -r session_name : using this command you will enter in your session. that is called Reattach to screen </li>
  <li>3. screen -ls : using this command , you will get all screen list with session name along with unique ids</li>
  <li>4. ctrl+a d   : you can detach from screen</li>
  <li>5. man screen : To check for the manual page of screen command.</li>
  <li>6. Ctrl+A ? : you will see all commands or parameters on the screen</li>
</ul>

